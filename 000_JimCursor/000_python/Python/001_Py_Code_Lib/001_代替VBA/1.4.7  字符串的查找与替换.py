a1 = 'abca'.find('a')     # 查找第一个位置
a2 = 'abca'.rfind('a')     # 查找最后一个位置
a3 = 'abca'.count('a')     # 在字符串中出现的次数
a4 = 'abca'.startswith('ab')    # 判断首次出现的字符, 返回True or False
a5 = 'abca'.endswith('a')    # 判断结束的字符, 返回True or False
a6 = 'abcadeafagahaiajklmjim'.replace('a','0000',2)    # 判断结束的字符, 返回True or False


print(a1)
print(a2)
print(a3)
print(a4)
print(a5)
print(a6)