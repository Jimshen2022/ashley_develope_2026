# of Transactions	Sum of Transactions from Tran. Log by Work Day
Trans. per Hour	Sum of Transactions from Tran. Log by Work Day divided by TTIW
Gap Time (Hrs)	Sum of time from End Time of 1 transaction to the Start Time of the next transaction through the work day divided by TTIW minus all Process Code: Break & Lunch scans
TTIW	Time from each Clock In to Clock Out segment for the work day-All punches that account for any time off the clock are factored in.  Additionally, any Process Code: Lunch scans subtract off.
F1 Exceptions	Sum of Exceptions logged for DCF1
F3 Exceptions	Sum of Exceptions logged for DCF3
F7 Exceptions	Sum of Exceptions logged for 303F7
F8 Exceptions	Sum of Exceptions logged for DCF8
F6 Exceptions	Sum of Exceptions logged for DCF6
Days of Service	Today minus Date of Hire
Headcount	Sum of associates clocked in
# of Battery Changes	Sum of Transaction: 500
Avg Minutes of Battery Change	Average minutes logged in transaction: 500 by associate
Total Minutes of Battery Change	Total minutes logged in transaction: 500