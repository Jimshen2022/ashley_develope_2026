  JOIN (select itnbr, itcls, B2Z95S, WEGHT, stid from MasterData_ItemMaster_WNK.ITMRVA where stid = '35') c ON a.WCIITEMNUMBER = c.itnbr 
    WHERE a.WCIORIGIN IN ('35','33')
