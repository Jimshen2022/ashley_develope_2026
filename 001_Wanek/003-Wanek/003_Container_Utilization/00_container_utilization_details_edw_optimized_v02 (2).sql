        LTRIM(RTRIM(a.WCIITEMNUMBER)), 
        a.WCIQUANTITYLOADED,
        a.WCILASTMAINTENANCETIMESTAMP, 
        a.WCILASTMAINTENANCEUSER, 
        b.ITMCQTY, 
        c.itcls, 
        c.B2Z95S, 
        c.WEGHT, 
        a.WCIQUANTITYLOADED * c.B2Z95S,
        CEILING(CAST(a.WCIQUANTITYLOADED AS FLOAT) / b.ITMCQTY),
