        WHEN ISNULL(t1.SCOOPQTY,0)*ISNULL(t3.ITMWEGHT,0) < 1700 THEN 'R. 1600-1700'
        WHEN ISNULL(t1.SCOOPQTY,0)*ISNULL(t3.ITMWEGHT,0) < 1800 THEN 'S. 1700-1800'
