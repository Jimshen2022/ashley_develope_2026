                  b.UNITLAYERS, b.UNITSDEEP, b.SCOOPQTY, b.SKIDSIZE, b.house  
           FROM MasterData_ItemMaster_AFI.ITBEXT as b 
           WHERE b.House in ('335')) AS t1 
    ON t1.itnbr = t2.itnbr and t1.house = t2.house
LEFT JOIN (SELECT c.ITNBR, c.QTYCR, c.NBSEAT, c.CRTWIN, c.CRTLIN, c.CRTHIN, 
                  c.PRDWIN, c.PRDHIN, c.PRDLIN, c.ITMWEGHT  
           FROM MasterData_ItemMaster_AFI.ITMEXT AS c) AS T3 
    ON T3.ITNBR = T2.ITNBR
LEFT JOIN (SELECT d.ITNBR, d.B2Z95S, d.ITDSC, d.STID  
           FROM MasterData_ItemMaster_AFI.ITMRVA AS d 
           WHERE d.STID in ('335')) AS t4 
    ON t4.itnbr = t2.itnbr and t4.stid = t2.house
WHERE T2.itnbr in ('3070638','3070635','3070620','2550438','2000635','2000535')