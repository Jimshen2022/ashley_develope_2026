        WHEN t3.ITMWEGHT*0.453592 < 180 THEN 'S. 170-180'
        ELSE 'T. Over 180' END) AS Unit_Weight_Range,
    (CASE 
        WHEN ISNULL(t1.SCOOPQTY,0)*ISNULL(t3.ITMWEGHT,0) < 100 THEN 'A. 0-100'
        WHEN ISNULL(t1.SCOOPQTY,0)*ISNULL(t3.ITMWEGHT,0) < 200 THEN 'B. 100-200'
