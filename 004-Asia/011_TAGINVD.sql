SELECT
    CAST(T1.TDTAG# AS VARCHAR(255)) AS SN,
    T1.TDITEM,
    T1.TDAPO#,
    T1.TDPDAT,
    T1.TDTSTS,
    T1.TDWHSE,
    CONCAT(T1.TDAREA, T1.TDASLE, T1.TDSEC, T1.TDTIER) AS LOCATION,
    T1.TDVDAT_DATE AS RECEIVED_DATE,
    T1.TDMDAT_DATE AS LAST_MOVING_DATE_IN_HJ,
    T1.TDPDAT_DATE AS PO_DUE_DATE_Nee

FROM Manufacturing_Inventory_AFI.TAGINVD AS T1
WHERE T1.TDWHSE = '335'
  AND T1.TDTSTS IN ('R')
  AND EXISTS (
      SELECT 1
      FROM (SELECT '1800412' AS ItemNo
            UNION ALL
            SELECT '1800512'
            UNION ALL
            SELECT '4710212'
            UNION ALL
            SELECT '4710312') AS Items
      WHERE Items.ItemNo = T1.TDITEM
  )


